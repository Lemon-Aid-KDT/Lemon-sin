def seed_spc_data():
    """SPC 합성 CSV 데이터 생성"""
    spc_dir = Path("data/spc_samples")
    spc_dir.mkdir(parents=True, exist_ok=True)

    for proc in SPC_PROCESSES:
        filepath = spc_dir / proc["filename"]
        values = []
        for i in range(proc["n_samples"]):
            # 현실적 패턴: 약간의 drift + 정규분포
            drift = 0.001 * (i - proc["n_samples"] / 2)  # 미세 트렌드
            v = random.gauss(proc["target"] + drift, proc["std_dev"])
            # 가끔(2%) 이상값 발생
            if random.random() < 0.02:
                v += random.choice([-1, 1]) * proc["std_dev"] * 3
            values.append(round(v, 4))

        # CSV 작성
        lines = [f"sample,value,unit,part_number,process"]
        for i, v in enumerate(values, 1):
            lines.append(f"{i},{v},{proc['unit']},{proc['part']},{proc['name']}")

        filepath.write_text("\n".join(lines), encoding="utf-8")

    # 메타 정보 JSON
    meta = []
    for proc in SPC_PROCESSES:
        meta.append({
            "name": proc["name"],
            "filename": proc["filename"],
            "target": proc["target"],
            "usl": proc["usl"],
            "lsl": proc["lsl"],
            "unit": proc["unit"],
            "part_number": proc["part"],
            "n_samples": proc["n_samples"],
        })
    (spc_dir / "spc_meta.json").write_text(
        json.dumps(meta, ensure_ascii=False, indent=2), encoding="utf-8"
    )

    print(f"  SPC 데이터 생성 완료: {len(SPC_PROCESSES)}개 공정 × {SPC_PROCESSES[0]['n_samples']}건")
    return len(SPC_PROCESSES)


